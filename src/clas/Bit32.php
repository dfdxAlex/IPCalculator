               32=>1,
               31=>2,
               30=>4,
               29=>8,
               28=>16,
               27=>32,
               26=>64,
               25=>128,
               24=>256,
               23=>512,
               22=>1024,
               21=>2048,
               20=>4096,
               19=>8192,
               18=>16384,
               17=>32768,
               16=>65536,
               15=>131072,
               14=>262144,
               13=>524288,
               12=>1048576,
               11=>2097152,
               10=>4194304,
               9=>8388608,
               8=>16777216,
               7=>33554432,
               6=>67108864,
               5=>134217728,
               4=>268435456,
               3=>536870912,
               2=>1073741824,
               1=>2147483648,
           };
      }
 }
